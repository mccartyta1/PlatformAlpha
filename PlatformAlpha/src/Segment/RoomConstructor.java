	private void resetObjects() {
		walls = new ArrayList<Actor>();
		enemies = new ArrayList<Actor>();
	}
	
	public ArrayList<Actor> getWalls() {
		return walls;
	}
	public ArrayList<Actor> getEnemies() {
		return enemies;
	}
	public Double[] getPlayerStartingPosition() {
		return playerStartingPosition;
	}
	
	private void addFloorAtY( double y, double beginX, int roomWidth ) {
		Wall wall;
		for ( int i = 0; i < (roomWidth / 32); i++ )
		{
			wall = new Wall( beginX + (i * 32) , y);
			walls.add( wall );
		}
	}
	
	private void createTowerAtX( double x, double beginY, int roomHeight ) {
		Wall wall;
		for ( int i = 0; i < (roomHeight / 32); i++ )
		{
			wall = new Wall( x, beginY + (i * 32) );
			walls.add( wall );
		}
	}
	
	private void createWall( double x, double y) {
		Wall wall = new Wall( x, y );
		walls.add( wall );
	}
	
	private void createExit( double x, double y, int level ) {
		Exit exit = new Exit( x, y, level );
		walls.add( exit );
	}
	
	private void createExit( double x, double y, int wi, int hi, int level ) {
		Exit exit = new Exit( x, y, wi, hi, level );
		walls.add( exit );
	}
	
	private void createEnemyGrunt( double x, double y, int direction) {
		Enemy enemy = new EnemyGrunt( x, y, direction, segment );
		enemies.add( enemy );
	}
	private void createEnemyWight( double x, double y, int direction) {
		Enemy enemy = new EnemyWight( x, y, direction, segment );
		enemies.add( enemy );
	}
	private void createCitizen( double x, double y, int direction, String[] messages ) {
		Citizen citizen = new Citizen( x, y, direction, segment );
		citizen.setMessages( messages );
		enemies.add( citizen );
	}
	
	private void setPlayerPosition( double x, double y ) {
		playerStartingPosition[0] = x;
		playerStartingPosition[1] = y;
	}

